package com.mcgui.javagui;

public class LayerParams {
    double mua, mus, g, n, z_top, z_bot;
    LayerParams(double mua, double mus, double g, double n, double z_top, double z_bot){
        this.mua=mua;
        this.mus=mus;
        this.g=g;
        this.n=n;
        this.z_bot=z_bot;
        this.z_top=z_top;
    }
}
